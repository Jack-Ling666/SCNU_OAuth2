package com.fx.pojo;

public class UserInfo {
	

	private int pid;
	private String userId;
	private String userName;
	private String password;
	private String user_dept1;
	private String user_dept2;
	private String user_kind;
	private String email;
	private String email_flag;
	private String phone;
	private String others;
	
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getUser_dept1() {
		return user_dept1;
	}
	public void setUser_dept1(String user_dept1) {
		this.user_dept1 = user_dept1;
	}
	public String getUser_dept2() {
		return user_dept2;
	}
	public void setUser_dept2(String user_dept2) {
		this.user_dept2 = user_dept2;
	}
	public String getUser_kind() {
		return user_kind;
	}
	public void setUser_kind(String user_kind) {
		this.user_kind = user_kind;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getEmail_flag() {
		return email_flag;
	}
	public void setEmail_flag(String email_flag) {
		this.email_flag = email_flag;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getOthers() {
		return others;
	}
	public void setOthers(String others) {
		this.others = others;
	}
	
	

}
